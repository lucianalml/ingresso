    public function store(Request $request)
    {
        $validator = Validator::make($request->all(), 
            ['cartao' => 'required',
            ]);

        if ($validator->fails()) {

            return back()->withErrors($validator)
                        ->withInput();
        }
        else
        {   
            
            flash()->success('Parabens! Pagamento do pedido '. 
                $request->get('pedido'). ' confirmado!');

            return redirect('/');
        }

    }

